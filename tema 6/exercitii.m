%% Ex1
X=[0.4 0.5 0.7 0.8];
f=@(x)log(x);
Y=f(X);
xgraf=linspace(0.4,0.8,200);
[ygraf]=SplineL(X,Y,xgraf);
figure(1);
fplot(f,[0.4 0.8]);
hold on;
grid on;
plot(xgraf,ygraf,"--r","LineWidth",3);
plot(X,Y,"o");
type('SplineL');
%% Ex2
X=[0.4 0.5 0.7 0.8];
f=@(x)log(x);
Y=f(X);
fpa=(Y(2)-Y(1))/(X(2)-X(1));
xgraf=linspace(0.4,0.8,200);
[ygraf zgraf]=SplineP(X,Y,xgraf,fpa);
figure(2);
fplot(f,[0.4,0.8]);
hold on;
grid on;
plot(xgraf,ygraf,"--b","LineWidth",3);
plot(X,Y,"o");
figure(3);
plot(xgraf,zgraf);
type('SplineP');
